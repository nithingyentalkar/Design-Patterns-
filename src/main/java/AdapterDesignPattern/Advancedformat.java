package AdapterDesignPattern;

public class Advancedformat {
    String advancedFileToPlay;
    String advancedFormatToPlay;
    String Resolution;

    public String getAdvancedFileToPlay() {
        return advancedFileToPlay;
    }

    public void setAdvancedFileToPlay(String advancedFileToPlay) {
        this.advancedFileToPlay = advancedFileToPlay;
    }

    public String getAdvancedFormatToPlay() {
        return advancedFormatToPlay;
    }

    public void setAdvancedFormatToPlay(String advancedFormatToPlay) {
        this.advancedFormatToPlay = advancedFormatToPlay;
    }

    public String getResolution() {
        return Resolution;
    }

    public void setResolution(String resolution) {
        Resolution = resolution;
    }
}
